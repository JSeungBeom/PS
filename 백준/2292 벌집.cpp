/*
위의 그림과 같이 육각형으로 이루어진 벌집이 있다.
그림에서 보는 바와 같이 중앙의 방 1부터 시작해서 이웃하는 방에 돌아가면서
1씩 증가하는 번호를 주소로 매길 수 있다.
숫자 N이 주어졌을 때, 벌집의 중앙 1에서 N번 방까지 최소 개수의 방을 지나서 갈 때
몇 개의 방을 지나가는지(시작과 끝을 포함하여)를 계산하는 프로그램을 작성하시오
예를 들면, 13까지는 3개, 58까지는 5개를 지난다.
*/

#include <iostream>

using namespace std;

int main() {
	int n;

	cin >> n;

	int path = 0; // 최단경로를 저장할 변수
	int num = 1; // 최단경로로 갈 수 있는 방 중, 가장 큰 값의 번째를 가지는 방을 저장할 변수

	do {
		num += 6 * path;
		path++;

	} while (num < n); // 최단 경로로 갈 수 있는 방의 최대 값은 1, 7, 19, ... 식으로 증가하므로, 이를 반복하며 path를 1씩 더해준다
	// num과 n이 같은 경우가 있을 수 있으므로, do ~ while문을 사용

	cout << path << endl;

	return 0;
}